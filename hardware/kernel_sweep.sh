run_fixed_point() {
  local app=$1 pa=$2 tag=$3 label=$4
  log="$OUT/${tag}.log"
  ( timeout 3600 make sim app="$app" hdv_plusargs="$pa +HDV_EXPECTED_EP=8000000" > "$log" 2>&1 )
  local r c pfa pfh sblk
  r=$(grep -iE 'mock host' "$log" | grep -oE 'PASSED|FAILED' | head -1)
  c=$(grep -iE 'mock host' "$log" | grep -oE 'cycle=[0-9]+' | grep -oE '[0-9]+' | head -1)
  pfa=$(kv 'PERF-ADDRGEN\]' 'pf_ar')
  pfh=$(kv 'PERF-ADDRGEN\]' 'pf_hit')
  sblk=$(kv 'PERF-SEQ' 'blocked')
  printf "%-34s %-7s cyc=%-8s pf=%-9s seq_blk=%s\n" \
         "$label" "${r:-TIMEOUT}" "${c:-NA}" "${pfa:--}->${pfh:--}" "${sblk:--}"
  echo "$KERNEL_ROWHDR" > "$OUT/${tag}.csv"
  write_kernel_csv_row "fixed" "$tag" "$tag" "" "" "" "" 0 0 "$OUT/${tag}.csv"
}

