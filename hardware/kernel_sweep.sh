AVL_ROWHDR="$KERNEL_ROWHDR"

run_1d_sweep() {
  local avls="${1:-32 64 128 256 512 1024 2048 4096}"
  local kernels="${2:-vsaxpy_hdv vvaddint32_hdv vscopy_hdv vsswap_hdv vsdot_hdv vsscal_hdv vmc_hdv dropout_hdv vsdwt_hdv}"

  declare -A maxavl=(
    [vsaxpy_hdv]=1024   [vscopy_hdv]=1024   [vsscal_hdv]=1024
    [vsswap_hdv]=1024   [vsdot_hdv]=1024    [vvaddint32_hdv]=4096
    [vmc_hdv]=1024      [dropout_hdv]=1024  [vsdwt_hdv]=4096
  )
  declare -A avlreg=( [vsdot_hdv]=A2 [vsdwt_hdv]=A2 )
  declare -A extra_plusargs=()

  note "########## Group 1: active 1D streaming kernels ##########"

  local k n csv max reg row
  for k in $kernels; do
    case "$k" in
      fdotp_hdv|vspf_*_hdv)
        echo "ERROR: $k is excluded from active sweep." >&2
        exit 2
        ;;
    esac
    max=${maxavl[$k]:-1024}
    csv="$OUT/${k}.csv"
    echo "$AVL_ROWHDR" > "$csv"
    echo "==================== $k  (max AVL=$max) ===================="
    printf "%-7s %-7s %-8s %-7s %-6s %-7s %-7s %-9s %s\n" \
           "AVL" "result" "cycles" "cyc/el" "EPs" "pkts" "avg/pk" "pf_ar->hit" "seq_blk"

    rm -f "$APPS/$k/main.c.o" "$APPS/bin/$k"
    if ! ( cd "$APPS" && timeout 600 make "bin/$k" ) > "$OUT/build_avl_${k}.log" 2>&1; then
      echo "  $k: BUILD FAIL (see $OUT/build_avl_${k}.log)"
      continue
    fi

    for n in $avls; do
      if [ "$n" -gt "$max" ]; then continue; fi
      log="$OUT/log_avl_${k}_${n}.log"
      reg=${avlreg[$k]:-A0}
      timeout 3600 make sim app="$k" \
        hdv_plusargs="+HDV_${reg}=${n} ${extra_plusargs[$k]:-} +HDV_EXPECTED_EP=8000000" \
        > "$log" 2>&1

      write_kernel_csv_row "avl" "$k" "" "$n" "" "" "" "$n" 0 "$csv"

      local r tc eps cpe pk acp pfa pfh sblk
      r=$(grep -E 'mock host' "$log" | head -1 | grep -oE 'PASSED|FAILED'); [ -z "$r" ] && r="ERR"
      tc=$(kv 'mock host' 'total_task_cycles')
      eps=$(d 'HDV-CSR.*DONE' 'accepted'); [ -z "$eps" ] && eps=$(grep -E 'mock host' "$log" | grep -oE 'got [0-9]+' | grep -oE '[0-9]+')
      cpe="-"; [ -n "$tc" ] && cpe=$(echo "scale=3; $tc/$n" | bc)
      pk=$(kv 'IPU-PERF' 'packets'); acp=$(kv 'IPU-PERF' 'avg_cycles_per_pkt')
      pfa=$(kv 'PERF-ADDRGEN\]' 'pf_ar')
      pfh=$(kv 'PERF-ADDRGEN\]' 'pf_hit')
      sblk=$(kv 'PERF-SEQ' 'blocked')
      printf "%-7s %-7s %-8s %-7s %-6s %-7s %-7s %-9s %s\n" \
             "$n" "$r" "${tc:--}" "$cpe" "${eps:--}" "${pk:--}" "${acp:--}" "${pfa:--}->${pfh:--}" "${sblk:--}"
    done
  done
}

