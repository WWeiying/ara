BLAS_ROWHDR="$KERNEL_ROWHDR"
declare -A BLAS_TAG_INIT=()

ensure_blas_tag_csv() {
  local tag=$1
  if [ -z "${BLAS_TAG_INIT[$tag]+x}" ]; then
    if [ "$APPEND_OUT" = "1" ] && [ -s "$OUT/${tag}.csv" ]; then
      :
    else
      echo "$BLAS_ROWHDR" > "$OUT/${tag}.csv"
    fi
    BLAS_TAG_INIT[$tag]=1
  fi
}

drop_existing_blas_row() {
  local tag=$1 size=$2 rows=$3 n=$4 csv="$OUT/${tag}.csv"
  if [ "$APPEND_OUT" != "1" ] || [ ! -s "$csv" ]; then
    return
  fi
  local tmp
  tmp=$(mktemp)
  awk -F, -v tag="$tag" -v size="$size" -v rows="$rows" -v n="$n" \
    'NR == 1 || !($2 == tag && $5 == size && $6 == rows && $7 == n)' \
    "$csv" > "$tmp"
  mv "$tmp" "$csv"
}

parse_blas_perf() {
  local tag=$1 size=$2 rows=$3 n=$4 macc=$5
  ensure_blas_tag_csv "$tag"
  drop_existing_blas_row "$tag" "$size" "$rows" "$n"
  write_kernel_csv_row "blas" "$tag" "$tag" "" "$size" "$rows" "$n" 0 "$macc" "$OUT/${tag}.csv"

  local r tc eps cpm vqmax pfa pfh sblk
  r=$(grep -E 'mock host' "$log" | head -1 | grep -oE 'PASSED|FAILED'); [ -z "$r" ] && r="ERR"
  tc=$(kv 'mock host' 'total_task_cycles')
  eps=$(d 'HDV-CSR.*DONE' 'accepted'); [ -z "$eps" ] && eps=$(grep -E 'mock host' "$log" | grep -oE 'got [0-9]+' | grep -oE '[0-9]+')
  cpm="-"; [ -n "$tc" ] && [ "$macc" -gt 0 ] 2>/dev/null && cpm=$(echo "scale=4; $tc/$macc" | bc)
  vqmax=$(kv 'HDV-PERF' 'vq_max_occupancy')
  pfa=$(kv 'PERF-ADDRGEN\]' 'pf_ar')
  pfh=$(kv 'PERF-ADDRGEN\]' 'pf_hit')
  sblk=$(kv 'PERF-SEQ' 'blocked')
  printf "%-18s %-5s %-5s %-7s %-8s %-9s %-7s %-7s %-9s %s\n" \
         "$tag" "$size" "$rows" "$r" "${tc:--}" "$cpm" "${eps:--}" "${vqmax:--}" \
         "${pfa:--}->${pfh:--}" "${sblk:--}"
}

sweep_vsgemm() {
  local ns="${1:-16 32 64 128}"
  note "########## Group 2A: vsgemm ##########"
  echo "==================== vsgemm  {lmul}x{rows}x{N} ===================="
  printf "%-18s %-5s %-5s %-7s %-8s %-9s %-7s %-7s %-9s %s\n" \
         "tag" "N" "rows" "result" "cycles" "cyc/macc" "EPs" "vq_max" "pf_ar->hit" "seq_blk"

  local lmul rows n nlist tag s1 s2 macc
  for lmul in 1 4; do
    nlist="$ns"
    [ "$lmul" = "1" ] && nlist="32"
    for rows in 1 2 4; do
      rm -f "$APPS/vsgemm_hdv/main.c.o"
      ( cd "$APPS" && timeout 600 make bin/vsgemm_hdv gemm_lmul=$lmul gemm_rows=$rows ) \
        > "$OUT/build_vsgemm_m${lmul}_${rows}r.log" 2>&1
      s1=$(addr_of vsgemm_hdv src1); s2=$(addr_of vsgemm_hdv src2)
      if [ -z "$s1" ] || [ -z "$s2" ]; then
        echo "  vsgemm m${lmul} ${rows}r: BUILD/ADDR FAIL"
        continue
      fi
      for n in $nlist; do
        tag="vsgemm_m${lmul}_${rows}r"
        log="$OUT/log_blas_${tag}_${n}.log"
        if [ "$SKIP_LONG" = "1" ] && [ "$lmul" = "4" ] && [ "$n" -ge 128 ]; then
          skip_long_note "$tag N=$n"
          printf "%-18s %-5s %-5s %-7s %-8s %-9s %-7s %-7s %-9s %s\n" \
                 "$tag" "$n" "$rows" "SKIP" "-" "-" "-" "-" "-->-" "over_10min"
          continue
        fi
        if { [ "$lmul" = "4" ] && [ "$rows" != "4" ] && [ "$n" -ge 64 ]; } ||
           { [ "$lmul" = "4" ] && [ "$rows" = "4" ] && [ "$n" -ge 128 ]; }; then
          timeout 3600 make sim app=vsgemm_hdv \
            hdv_plusargs="+HDV_A0=$s1 +HDV_A1=$s2 +HDV_A2=$s1 +HDV_A3=$n +HDV_EXPECTED_EP=8000000 +HDV_TASK_WATCHDOG=800000" \
            > "$log" 2>&1
        else
          timeout 600 make sim app=vsgemm_hdv \
            hdv_plusargs="+HDV_A0=$s1 +HDV_A1=$s2 +HDV_A2=$s1 +HDV_A3=$n +HDV_EXPECTED_EP=8000000" \
            > "$log" 2>&1
        fi
        macc=$((n*n*n))
        parse_blas_perf "$tag" "$n" "$rows" "$n" "$macc"
      done
    done
  done
}

sweep_blas_dim() {
  local ns="${1:-16 32 64 128}"
  note "########## Group 2B: BLAS dim sweep ##########"

  declare -A b_dimreg=( [vssymv_hdv]=A3 [vssyrk_hdv]=A2 [vstrsm_hdv]=A2
                        [vsger_hdv]=A1  [vsgemv_hdv]=A3 )
  local k n s1 s2 reg ptrs macc
  for k in vsger_hdv vssymv_hdv vssyrk_hdv vstrsm_hdv vsgemv_hdv; do
    rm -f "$APPS/$k/main.c.o"
    ( cd "$APPS" && timeout 600 make bin/$k blas_lmul=4 ) > "$OUT/build_blas_${k}.log" 2>&1
    s1=$(addr_of "$k" src1); s2=$(addr_of "$k" src2)
    if [ -z "$s1" ]; then
      echo "  $k: BUILD/ADDR FAIL"
      continue
    fi
    echo "==================== $k  (dim sweep) ===================="
    printf "%-18s %-5s %-5s %-7s %-8s %-9s %-7s %-7s %-9s %s\n" \
           "tag" "size" "rows" "result" "cycles" "cyc/macc" "EPs" "vq_max" "pf_ar->hit" "seq_blk"
    reg=${b_dimreg[$k]}
    for n in $ns; do
      case "$k" in
        vsger_hdv)  ptrs="+HDV_A0=32 +HDV_A2=$s2 +HDV_A3=$((s2+512)) +HDV_A4=$s1 +HDV_A1=$n" ;;
        vssymv_hdv) ptrs="+HDV_A0=$s1 +HDV_A1=$s2 +HDV_A2=$s1 +HDV_A3=$n" ;;
        vssyrk_hdv) ptrs="+HDV_A0=$s1 +HDV_A1=$s2 +HDV_A2=$n" ;;
        vstrsm_hdv) ptrs="+HDV_A0=$s1 +HDV_A1=$s2 +HDV_A2=$n" ;;
        vsgemv_hdv) ptrs="+HDV_A0=$s1 +HDV_A1=$s2 +HDV_A2=$((s2+512)) +HDV_A3=$n" ;;
      esac
      log="$OUT/log_blas_${k}_${n}.log"
      if [ "$SKIP_LONG" = "1" ] &&
         { { [ "$k" = "vssyrk_hdv" ] && [ "$n" -ge 64 ]; } ||
           { [ "$k" = "vstrsm_hdv" ] && [ "$n" -ge 128 ]; }; }; then
